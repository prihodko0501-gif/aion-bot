def answer_rules(question: str) -> str:
    """
    Простые правила ответов помощника AION
    """

    q = question.lower()

    if "сон" in q:
        return "Сон — главный восстановительный механизм организма."

    if "стресс" in q:
        return "Хронический стресс ускоряет биологический износ."

    if "восстановление" in q:
        return "Восстановление — ключ к управлению биологическим возрастом."

    return "Задай вопрос подробнее."
