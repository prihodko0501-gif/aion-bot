def pressure_zone(sys: int, dia: int) -> tuple[str, str, float]:
    """
    Возвращает: (zone, comment, penalty)
    MVP безопасная логика.
    """

    # Опасно низкое
    if sys < 90 or dia < 60:
        return ("Красная", "Стоп. Только восстановление, контроль давления.", 2.0)

    # Высокое
    if sys >= 140 or dia >= 90:
        return ("Красная", "Стоп. Только восстановление. Контроль давления.", 2.0)

    # Пограничное
    if sys >= 130 or dia >= 85:
        return ("Жёлтая", "Осторожно. Умеренный режим, без добивания.", 1.0)

    return ("Зелёная", "Ок. Можно по плану.", 0.0)
